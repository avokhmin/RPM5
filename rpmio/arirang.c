#define BIG_D2B(D, B)		*(uint32_t *)(B) = (uint32_t)(D)
#define LITTLE_B2D(B, D)	D = ENDIAN_REVERSE_DWORD(*(uint32_t *)(B))
#define LITTLE_D2B(D, B)	*(uint32_t *)(B) = ENDIAN_REVERSE_DWORD(D)

